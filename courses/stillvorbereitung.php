<p>
Stillen bietet einem Baby genau das, was es in seinen ersten Lebensmonaten braucht: alle wichtigen Nährstoffe, körperliche Nähe, Zuwendung und Unterstützung für sein Immunsystem und für seine Entwicklung. 
</p>

<p>
Doch wie genau funktioniert das Stillen überhaupt? 
</p>

<p>
Der Stillvorbereitungskurs bietet: 
<ul>
  <li>wertvolle Informationen</li>
  <li>praktische Übungen</li>
  <li>vorstellung verschiedener Utensilien</li> 
  <li>Handouts</li>
  <li>und ein offenen Austausch</li> 
</ul>
</p>

<p>
Der Kurs kann wunderbar ergänzend zum Geburtsvorbereitungskurs besucht werden. Jeder der Lust hat, auch werdende Väter, ist im Stillvorbereitungskurs herzlich willkommen.
</p>
