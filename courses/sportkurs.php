<p>
    Dieser Kurs bietet ein effektives Ganzkörpertraining bei dem alle wichtigen Muskelgruppen gekräftigt werden. 
    Es wird spaßig und abwechslungsreich. 
    Im Kurs werden Krafttraining, Cardio und Flexibilität kombiniert, um die Ausdauer, Kraft und Beweglichkeit zu trainieren.
</p>

<p>
Der Kurs richtet sich an alle, die Lust haben etwas für ihre Fitness zu tun. 
Er ist für alles Fitnesslevel geeignet, da die Intensität und die Schwierigkeit der Übungen an deine individuellen Bedürfnisse angepasst werden können.
</p>

<p>
Voraussetzung für die Teilnahme am Kurs ist ein abgeschlossener Rückbildungskurs.
</p>