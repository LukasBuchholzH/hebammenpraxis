<p>
    Neugierig auf Stoffwindeln?
</p>
<p>
    Im Workshop lernt ihr in einer gemütlichen Runde alles, was ihr über
    moderne Stoffwindeln und das Wickeln mit Stoff wissen müsst.
    Verschiedene Stoffwindelsysteme, Materialien, die richtige Handhabung
    sowie Waschen und Lagern von Stoffwindeln. Ihr habt die Chnace
    unterschiedliche Stoffwindeln live kennenzulernen, dürft alles an einem
    Wickeldummy ausprobieren und fleißig Probe wickeln. Gemeinsam üben wir,
    wie wir auch Mullwindeln zum Wickeln nutzen und falten können.
</p>
<p>
    Am Ende habt ihr die Möglichkeit Fragen zu stellen, sodass wir mögliche
    Unsicherheiten gemeinsam überwinden können. Nach dem Kurs erhaltet ihr
    eine Zusammenfassung als Skript und seid optimal vorbereitet eure
    Stoffwindelreise zu beginnen.
</p>