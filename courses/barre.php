<strong>Was ist barre concept®?</strong>

<p>
barre concept® ist ein Workout mit Elementen aus dem Ballett, Pilates, Yoga und Gewichttraining. 
</p>

<p>
Dieses spezielle Ganzkörpertraining verbrennt Körperfett mit gezieltem Muskelaufbau. 
Arme, Beine, Bauch und Po werden straff, sehen trainiert und weiblich aus.
Ein optimales Training, welches die Körpermitte sowie den Beckenboden stärkt und aufbaut. 
Die Zentrierung der Körpermitte durch Anspannung des Bauches ist die Grundstellung für alle Übungen. 
</p>

<p>
Die Qualität der Bewegung wird durch Kontrolle gewährleistet. 
Bewegungen werden immer kontrolliert durchgeführt, um Verletzungen zu vermeiden und optimale Ergebnisse zu erzielen. 
Die Bewegungsabläufe sind klar und direkt. 
Ein dynamischer Bewegungsfluss ist für alle Übungen wichtig. 
Ruckartige Bewegungen werden vermieden. 
</p>

<p>
Ein perfektes Bodyworkout NACH abgeschlossenem Rückbildungskurs. 
Teilnahme in der Schwangerschaft nach Rücksprache im Einzelfall möglich.
</p>
