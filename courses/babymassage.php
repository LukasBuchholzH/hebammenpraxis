<p>
Babymassage dient zur Sinneserfahrung zur Erhaltung der Gesundheit und des Wohlbefindens des Kindes.
Die Babymassage kann sich positiv auf den Kreislauf, auf die Verdauung, das Atmungsorgan, die sprachliche Entwicklung und das Wachstum auswirken. 
Außerdem wirkt es sich positiv auf das Schlafen und Zahnen aus. 
</p>

<p>
In der Babymassage wollen wir gemeinsam in gemütlicher Runde die Kraft der Berührung als Kommunikationsmittel erlernen.
Nicht nur die Babys dürfen dabei entspannen, auch die Erwachsenen dürfen Durchatmen und Entspannen. 
Die Babymassage eignet sich ab der 6. Lebenswoche bis zum Eintritt ins Krabbelalter. 
</p>

<p>
Die Babymassage beinhaltet: Massage gegen Blähungen und Koliken, Ganzkörpermassage für Babys , mit dem Kind kommunizieren, den Bindungsprozess stärken, ein Skript nach jeder Stunde. 
</p>

